const index = new Map()
for (const icon of Object.values(simpleIcons)) {
  if (!icon || !icon.slug) continue
  if (!index.has(icon.slug)) index.set(icon.slug, icon)
  const tnorm = normalize(icon.title)
  if (tnorm && !index.has(tnorm)) index.set(tnorm, icon)
}

for (const icon of BRAND_OVERRIDES) {
  index.set(icon.slug, icon)
  index.set(normalize(icon.title), icon)
}

export function getBrandIcon(title) {
  const key = normalize(title)
  if (!key) return null
  return index.get(key) || null
}

export function isDarkHex(hex) {
  const r = parseInt(hex.slice(0, 2), 16)
  const g = parseInt(hex.slice(2, 4), 16)
  const b = parseInt(hex.slice(4, 6), 16)
  return 0.2126 * r + 0.7152 * g + 0.0722 * b < 60
}

export function hexToRgba(hex, alpha) {
  const r = parseInt(hex.slice(0, 2), 16)
  const g = parseInt(hex.slice(2, 4), 16)
  const b = parseInt(hex.slice(4, 6), 16)
  return `rgba(${r}, ${g}, ${b}, ${alpha})`
}

export function hueFromString(str) {
  let h = 0
  for (let i = 0; i < (str || '').length; i++) {
    h = (h * 31 + str.charCodeAt(i)) % 360
  }
  return h
}
